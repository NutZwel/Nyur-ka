export const MASCOT_NAMES: Record<string, string> = {
  whale: '🐋 Paus',
  fox: '🦊 Rubah',
  frog: '🐸 Katak',
  owl: '🦉 Burung Hantu',
  dragon: '🐉 Naga Kecil',
  cat: '🐱 Kucing',
}

interface Props {
  type: string
  size?: number
}

export default function PixelMascot({ type, size = 40 }: Props) {
  const [frameIndex, setFrameIndex] = useState(0)
  const theme = useThemeStore(s => s.theme)
  const { isPlaying } = usePlayerStore()

  const sprites = mascotSprites[type]
  const frames: Frames = sprites || mascotSprites.whale

  useEffect(() => {
    const speed = isPlaying ? 150 : 400
    const interval = setInterval(() => {
      setFrameIndex(i => (i + 1) % frames.length)
    }, speed)
    return () => clearInterval(interval)
  }, [isPlaying, type])

  const frame = frames[frameIndex]
  const ps = size / 16

  const pixelColor = (v: number) => {
    if (v === 1) return theme.primary
    if (v === 2) return theme.accent
    if (v === 3) return '#FFFFFF'
    if (v === 4) return theme.secondary
    return 'transparent'
  }

  return (
    <div style={{ width: size, height: size, position: 'relative', imageRendering: 'pixelated' }}>
      {frame.map((row, y) =>
        row.map((val, x) => {
          if (val === 0) return null
          return (
            <div
              key={`${y}-${x}`}
              style={{
                position: 'absolute',
                left: x * ps,
                top: y * ps,
                width: ps,
                height: ps,
                backgroundColor: pixelColor(val),
                borderRadius: val === 3 ? '50%' : 1,
                transition: 'background-color 0.3s ease',
              }}
            />
          )
        })
      )}
    </div>
  )
}
