interface Props {
  size?: number
  style?: React.CSSProperties
}

export default function DancingRobot({ size = 128, style }: Props) {
  const [frameIndex, setFrameIndex] = useState(0)
  const [bounce, setBounce] = useState(0)
  const { isPlaying } = usePlayerStore()
  const theme = useThemeStore(s => s.theme)

  useEffect(() => {
    if (!isPlaying) return
    const speed = 150
    const interval = setInterval(() => {
      setFrameIndex(i => (i + 1) % frames.length)
      setBounce(b => (b === 0 ? 1 : 0))
    }, speed)
    return () => clearInterval(interval)
  }, [isPlaying])

  const frame = frames[frameIndex]
  const pixelSize = size / FRAME_SIZE

  const pixels: JSX.Element[] = []
  for (let y = 0; y < FRAME_SIZE; y++) {
    for (let x = 0; x < FRAME_SIZE; x++) {
      const val = frame[y][x]
      if (val === 0) continue
      const color = val === 1 ? theme.primary : val === 2 ? theme.accent : '#FFFFFF'
      pixels.push(
        <div
          key={`${y}-${x}`}
          style={{
            position: 'absolute',
            left: x * pixelSize,
            top: y * pixelSize,
            width: pixelSize,
            height: pixelSize,
            backgroundColor: color,
            borderRadius: val === 3 ? '50%' : 1,
            transition: 'background-color 0.3s ease',
          }}
        />
      )
    }
  }

  return (
    <div
      style={{
        width: size,
        height: size,
        position: 'relative',
        imageRendering: 'pixelated',
        transform: `translateY(${bounce * -4}px)`,
        transition: 'transform 0.15s ease',
        opacity: isPlaying ? 1 : 0.3,
        ...style,
      }}
    >
      {pixels}
    </div>
  )
}
