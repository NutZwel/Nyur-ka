export default function PixelWhale({ size = 40, showWater = true }: { size?: number; showWater?: boolean }) {
  const [frameIndex, setFrameIndex] = useState(0)
  const theme = useThemeStore(s => s.theme)
  const { isPlaying } = usePlayerStore()

  useEffect(() => {
    const speed = isPlaying ? 200 : 450
    const interval = setInterval(() => setFrameIndex(i => (i + 1) % frames.length), speed)
    return () => clearInterval(interval)
  }, [isPlaying])

  const frame = frames[frameIndex]
  const pixelSize = size / 16
  const bodyColor = theme.primary
  const accentColor = theme.accent

  return (
    <div style={{ width: size, height: size, position: 'relative', imageRendering: 'pixelated' }}>
      {frame.map((row, y) =>
        row.map((val, x) => {
          if (val === 0) return null
          const color = val === 1 ? bodyColor : val === 2 ? accentColor : val === 3 ? '#FFFFFF' : '#60A5FA'
          const isWater = val === 9
          if (isWater && !showWater) return null
          return (
            <div
              key={`${y}-${x}`}
              style={{
                position: 'absolute',
                left: x * pixelSize,
                top: y * pixelSize + (isWater ? Math.sin(frameIndex * 0.5 + x) * 2 : 0),
                width: pixelSize,
                height: pixelSize,
                backgroundColor: color,
                borderRadius: val === 3 ? '50%' : 1,
                opacity: isWater ? 0.5 + Math.sin(frameIndex * 0.3 + y) * 0.3 : 1,
                transition: 'opacity 0.3s ease',
              }}
            />
          )
        })
      )}
    </div>
  )
}
